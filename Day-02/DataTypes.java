class DataTypes {
    public static void main(String args[]) {

        byte a = 10; // Range -128 to 128
        short b = 129; // Range byte - int
        int c = 10; // integer
        long d = 10; // For larger digits

        // decimal
        float e = 10.23456f;// float ranges for only 6 digits
        double f = 10.345;// upto 12 decimals

        // letters
        char t = 'a'; // a single character
        String s = "Deepak"; // String is a class with sequence of character

        // Boolean
        boolean k = true; // Used t declare true or false value

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(t);
        System.out.println(s);
        System.out.println(k);

    }
}